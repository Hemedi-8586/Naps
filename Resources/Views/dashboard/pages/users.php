NAPS Citizen Portal Final Structure

    1. Dashboard (Overview)

         Summary ya maombi na malipo.

    2. My Profile (Personal Data)

        NIDA Bio-data na mawasiliano.

    3. Service Applications (The Engine)

        Catalog ya huduma na Fomu za maombi.

    4. Application History (Tracking)

        Kufuatilia hatua za ombi (Submitted → Processing → Finished).

    5. My Payments (Financials)

        Control Numbers na Historia ya miamala.

    6. Activity Log (Security Audit)

        Login History: (Muda, kifaa alichotumia, na IP address).

        Action Logs: (Mfano: "Ulibadilisha neno la siri", "Ulituma ombi la huduma X").

    7. Notifications (Inbox)

        Alerts za mfumo na ujumbe.

    8. Help & Support

        FAQs na kuanzisha Ticket za msaada.

    9. Account Settings

        Usalama wa akaunti na mapendeleo (Preferences).

    10. Logout